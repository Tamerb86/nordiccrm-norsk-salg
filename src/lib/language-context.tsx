import { createContext, useContext, ReactNode } from 'react'
import { useKV } from '@github/spark/hooks'
import { norwegianTranslations } from './norwegian'
import { englishTranslations } from './english'

export type Language = 'no' | 'en'

type Translations = typeof norwegianTranslations

interface LanguageContextType {
  language: Language
  setLanguage: (lang: Language) => void
  t: Translations
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined)

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useKV<Language>('app-language', 'no')

  const currentLanguage: Language = language || 'no'
  const translations = currentLanguage === 'en' ? englishTranslations : norwegianTranslations

  return (
    <LanguageContext.Provider value={{ language: currentLanguage, setLanguage, t: translations }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}
